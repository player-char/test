function chance(a) {
	return Math.random() < a;
}

// –≤—ã—Ç–∞—Å–∫–∏–≤–∞–Ω–∏–µ —ç–ª–µ–º–µ–Ω—Ç–∞ –∏–∑ –º–∞—Å—Å–∏–≤–∞
function pick(arr, rand) {
	if (typeof rand == 'undefined') {
		// —Ç—É–ø–æ —Ä–∞–Ω–¥–æ–º–Ω–æ
		return arr[Math.floor(arr.length * Math.random())];
	} else {
		// –ø–æ –æ—Å—Ç–∞—Ç–∫—É
		return arr[rand % arr.length];
	}
}

