function replacePow(s) {
	
	function operand(pos, d) {
		let o = 0; // offset
		let lvl = 0;
		let c;
		
		while (c = s[pos + o + d]) {
			// parentheses
			let op = c == '(';
			let cl = c == ')';
			if (op || cl) {
				lvl += op * d - cl * d;
				if (lvl < 0) {
					lvl = 0;
					break;
				}
			}
			
			// stopping chars
			if (!lvl && ',/%*+-'.indexOf(c) != -1) {
				break;
			}
			
			o += d;
		}
		return !lvl * o;
	}
	
	while (true) {
		let pos = s.lastIndexOf('**');
		if (pos == -1) {
			return s;
		}
		let a = operand(pos, -1);
		let b = operand(pos + 1, 1);
		if (!(a * b)) {
			// operands are wrecked
			return false;
		}
		let outa = s.slice(0, pos + a);
		let sa = s.slice(pos + a, pos);
		let sb = s.slice(pos + 2, pos + 2 + b);
		let outb = s.slice(pos + 2 + b);
		
		s = outa + '(Math.pow((' + sa + '),(' + sb + ')))' + outb;
	}
}

