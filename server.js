// –∫—Ä–∏–ø—É—à–∫–∏ –æ—Ç–≤–µ—Ç –Ω–∞ —Å–æ–æ–±—â–µ–Ω–∏–µ
function finalReply(message, method, text, opt) {
	if (method != '+' && method != 'c' && (method != 'r' || !message.guild)) {
		// Capitalizing
		text = text.slice(0, 1).toUpperCase() + text.slice(1);
	}
	
	const f = {
		'r': () => message.reply(text, opt), // reply w/ @mention
		's': () => message.channel.send(text, opt), // just say
		'd': () => message.author.send(text, opt), // force private conversation
		'+': () => message.react(text), // put a reaction instead
		'c': () => customReact(message, text), // put a custom reaction
	};
	
	if (!f[method]) {
		console.log('Unknown reply type:', method);
		return false;
	}
	
	return f[method]();
}

