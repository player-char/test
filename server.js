			'–≤—Ä–µ–º—è —Ñ–ª—É–¥–∞ –æ–∫–æ–Ω—á–µ–Ω–æ, –¥–∞–≤–∞–π –∏–¥–∏ –æ—Ç–¥—ã—Ö–∞–π.',
			'—ç–π, –Ω–µ —Ç–∞–∫ –±—ã—Å—Ç—Ä–æ!',
			'–æ—Ö–ª–∞–¥–∏ –æ—Ç–ø—Ä–∞–≤–∫—É —Å–æ–æ–±—â–µ–Ω–∏–π, –æ–Ω–∞ —É —Ç–µ–±—è –ø–µ—Ä–µ–≥—Ä–µ–ª–∞—Å—å.',
